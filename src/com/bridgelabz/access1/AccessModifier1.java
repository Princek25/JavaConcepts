package com.bridgelabz.access1;

// 4 types of access modifier - default, public, private, protected
// default   - can access outside the class but cannot access outside the package
// public    - can access outside the class and package also
// private   - cannot access outside the class and package
// protected - can access outside the class but cannot access outside the package
// protected - can access outside the package through inheritance

public class AccessModifier1 {

    // default access modifier
    int a = 10;

    // public access modifier
    public int b = 20;

    // private access modifier
    private int c = 30;

    // protected access modifier
    protected int d = 40;

}
